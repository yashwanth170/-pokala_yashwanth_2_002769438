    private void DisplayPerson() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Person v : ms.getPersonList().getPersons()) {

            Period period = Period.between(v.getDob().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(), LocalDate.now());

            Object[] row = new Object[6];
            row[0] = v.getId();
            row[1] = v.getFullName();
            row[2] = v.getGender();
            row[3] = v.getResidence().getAddress();
            row[4] = v.getResidence().getCommunity().getCommunity();
            row[5] = period.getYears();

            model.addRow(row);
        }

    }
    
    public boolean validateData() {

        if (txtName.getText().length() < 2 || !txtName.getText().matches("[a-zA-Z_ ]+")) {
            JOptionPane.showMessageDialog(this, "Enter proper name.");
            return false;
        }

        if(!txtDob.getText().matches("^(0[1-9]|1[012])/(0[1-9]|[12][0-9]|3[01])/((19|2[0-9])[0-9]{2})$")){
             JOptionPane.showMessageDialog(this, "Enter proper date");
                return false;
                        
             } 
        
        
        return true;
    }

