    private void btnAddPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPatientActionPerformed
        // TODO add your handling code here:

        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please Select a row to Add Vital stats.");
            return;
        }

        btnAddPatient.setEnabled(true);
        Date resultdate = new Date(System.currentTimeMillis());
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        Person p = ms.searchUser((int) model.getValueAt(selectedRow, 0));

        System.out.println("temp: " + txtTemp.getText() + " " + TxtPulse.getText() + " " + txtBP.getText());
        VitalSigns vs = new VitalSigns(Integer.valueOf(txtTemp.getText()), Integer.valueOf(TxtPulse.getText()), Integer.valueOf(txtBP.getText()));
        Encounter enc = new Encounter(vs, resultdate, diag.getText());
        //fullName, residence, gender, dob, id
        Patient pat = new Patient(p.getFullName(), p.getResidence(), p.getGender(), p.getDob(), p.getId(), enc);
        ms.addPatient(pat);

        btnAddPatient.setEnabled(false);
    }//GEN-LAST:event_btnAddPatientActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please Select a row to delete.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        ms.deletePerson(Integer.valueOf(model.getValueAt(selectedRow, 0).toString()));
        DisplayPerson();
        JOptionPane.showMessageDialog(this, "Record Deleted!");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void varUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_varUpdateActionPerformed
        // TODO add your handling code here:
        int selectRow=jTable1.getSelectedRow();

        if(selectRow<0)
        {
            JOptionPane.showMessageDialog(this,"Please select a valid row");
            return;
        }
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        Person p=ms.searchUser((int)model.getValueAt(selectRow, 0));
        Patient pat=ms.searcPatient((int)model.getValueAt(selectRow, 0));
        if(validateData()){
            
            p.setFullName(txtName.getText());
            if(pat!=null)
                pat.setFullName(p.getFullName());
            String sss = female.isSelected() ? "female" : "male";

            p.setGender(sss);
            if(pat!=null)
                pat.setGender(sss);
            String dob=txtDob.getText();
            
            try{
                Date date1;
                date1 = new SimpleDateFormat("dd/MM/yyyy").parse(dob);
                p.setDob(date1);
                if(pat!=null)
                    pat.setDob(date1);
            }
            catch (ParseException ex) {
                
            }
           
            model.setValueAt(txtName.getText(),jTable1.getSelectedRow(),1);
            model.setValueAt(sss,jTable1.getSelectedRow(),2);
            Period period = Period.between(p.getDob().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(), LocalDate.now());
            model.setValueAt(period.getYears(),jTable1.getSelectedRow(),5);
            JOptionPane.showMessageDialog(this, " Updated ");
        }
    }//GEN-LAST:event_varUpdateActionPerformed

